const username =
  process.env.GITHUB_USERNAME ||
  process.env.GITHUB_REPOSITORY?.split("/")[0] ||
  "AlucPro";

const token = process.env.GITHUB_TOKEN;
const headers = {
  Accept: "application/vnd.github+json",
  "User-Agent": "alucpro-readme-stats",
  "X-GitHub-Api-Version": "2022-11-28",
};

if (token) {
  headers.Authorization = `Bearer ${token}`;
}

async function github(path) {
  const response = await fetch(`https://api.github.com${path}`, { headers });

  if (!response.ok) {
    const body = await response.text();
    throw new Error(`GitHub API ${response.status} for ${path}: ${body}`);
  }

  return response.json();
}

async function listRepos() {
  const repos = [];
  let page = 1;

  while (true) {
    const batch = await github(
      `/users/${encodeURIComponent(username)}/repos?type=owner&sort=updated&per_page=100&page=${page}`,
    );
    repos.push(...batch);

    if (batch.length < 100) {
      return repos;
    }

    page += 1;
  }
}

function escapeXml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function compactNumber(value) {
  return new Intl.NumberFormat("en", {
    notation: "compact",
    maximumFractionDigits: 1,
  }).format(value);
}

function metric(label, value, x, y, color) {
  return `
    <g transform="translate(${x} ${y})">
      <circle cx="8" cy="8" r="4" fill="${color}" />
      <text x="22" y="6" class="label">${escapeXml(label)}</text>
      <text x="22" y="30" class="value">${escapeXml(value)}</text>
    </g>`;
}

function renderSvg({ user, repos }) {
  const sourceRepos = repos.filter((repo) => !repo.fork);
  const stars = repos.reduce((sum, repo) => sum + repo.stargazers_count, 0);
  const forks = repos.reduce((sum, repo) => sum + repo.forks_count, 0);
  const languages = new Set(sourceRepos.map((repo) => repo.language).filter(Boolean));
  const updated = new Date().toISOString().slice(0, 10);

  return `<svg width="520" height="210" viewBox="0 0 520 210" fill="none" xmlns="http://www.w3.org/2000/svg" role="img" aria-labelledby="title desc">
  <title id="title">${escapeXml(username)} GitHub stats</title>
  <desc id="desc">Static GitHub profile statistics generated by GitHub Actions.</desc>
  <style>
    .card { fill: #ffffff; stroke: #d0d7de; }
    .title { fill: #24292f; font: 700 22px -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif; }
    .subtitle { fill: #57606a; font: 500 13px -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif; }
    .label { fill: #57606a; font: 600 12px -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif; text-transform: uppercase; }
    .value { fill: #24292f; font: 700 22px -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif; }
    @media (prefers-color-scheme: dark) {
      .card { fill: #0d1117; stroke: #30363d; }
      .title, .value { fill: #f0f6fc; }
      .subtitle, .label { fill: #8b949e; }
    }
  </style>
  <rect class="card" x="0.5" y="0.5" width="519" height="209" rx="8" />
  <text x="28" y="42" class="title">${escapeXml(user.name || username)}'s GitHub Stats</text>
  <text x="28" y="65" class="subtitle">@${escapeXml(username)} · updated ${escapeXml(updated)}</text>
  ${metric("Public repos", compactNumber(user.public_repos), 30, 95, "#2f81f7")}
  ${metric("Source repos", compactNumber(sourceRepos.length), 190, 95, "#3fb950")}
  ${metric("Stars", compactNumber(stars), 350, 95, "#d29922")}
  ${metric("Followers", compactNumber(user.followers), 30, 150, "#a371f7")}
  ${metric("Forks", compactNumber(forks), 190, 150, "#f85149")}
  ${metric("Languages", compactNumber(languages.size), 350, 150, "#db61a2")}
</svg>
`;
}

const [user, repos] = await Promise.all([github(`/users/${encodeURIComponent(username)}`), listRepos()]);
const svg = renderSvg({ user, repos });

await import("node:fs/promises").then((fs) =>
  fs.writeFile(new URL("../assets/github-stats.svg", import.meta.url), svg),
);
